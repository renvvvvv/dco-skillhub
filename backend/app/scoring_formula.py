SCORE_LEVELS = {
    "S级 (卓越)": (90, 100, "功能强大，广受欢迎，持续维护"),
    "A级 (优秀)": (75, 89, "质量较高，有一定使用量"),
    "B级 (良好)": (60, 74, "基本合格，需要更多推广"),
    "C级 (一般)": (40, 59, "功能简单，使用较少"),
    "D级 (待改进)": (0, 39, "需要完善文档和功能"),
}


def get_score_level(total_score):
    """根据总分获取等级"""
    for level, (min_score, max_score, desc) in SCORE_LEVELS.items():
        if min_score <= total_score <= max_score:
            return level, desc
    return "未知", ""


