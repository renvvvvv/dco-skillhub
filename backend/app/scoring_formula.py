# ============================================================
# Skill综合评分计算公式详解
# ============================================================

"""
总分 = 使用价值(35%) + 质量水平(25%) + 创新程度(20%) + 推广效果(15%) + 维护活跃度(5%)

每个维度满分100分，按权重计算后相加得到总分（满分100分）
"""

# ============================================================
# 1. 使用价值 (35分) - 衡量Skill被实际使用的程度
# ============================================================


def calculate_usage_score(stats):
    """
    使用价值 = min(下载量 × 2, 35)

    计算逻辑：
    - 每次下载得2分
    - 上限35分（即17.5次下载即可满分）

    示例：
    - Skill A: 下载10次 → 10 × 2 = 20分
    - Skill B: 下载20次 → min(20 × 2, 35) = 35分（满分）
    - Skill C: 下载0次 → 0分
    """
    return min(stats.get("total_downloads", 0) * 2, 35)


# ============================================================
# 2. 质量水平 (25分) - 衡量Skill的文档和代码质量
# ============================================================


def calculate_quality_score(skill_data):
    """
    质量水平 = README得分 + 标签得分 + 版本得分

    2.1 README得分 (最高10分)
    README得分 = min(README字数 / 100, 10)

    计算逻辑：
    - 每100字得1分
    - 上限10分（即1000字即可满分）

    示例：
    - Skill A: README 500字 → 500/100 = 5分
    - Skill B: README 2000字 → min(2000/100, 10) = 10分（满分）
    - Skill C: 无README → 0分


    2.2 标签得分 (最高10分)
    标签得分 = min(标签数量 × 2, 10)

    计算逻辑：
    - 每个标签得2分
    - 上限10分（即5个标签即可满分）

    示例：
    - Skill A: 标签["技术开发", "Agent开发"] → 2 × 2 = 4分
    - Skill B: 标签["技术开发", "Agent开发", "自动化开发", "数据分析", "运维支撑"] → 5 × 2 = 10分（满分）
    - Skill C: 无标签 → 0分


    2.3 版本得分 (最高5分)
    版本得分 = min(版本数量 × 5, 5)

    计算逻辑：
    - 每个版本得5分
    - 上限5分（即1个版本即可满分）

    示例：
    - Skill A: 1个版本 → 1 × 5 = 5分（满分）
    - Skill B: 3个版本 → min(3 × 5, 5) = 5分（满分）


    总分示例：
    - Skill A: README 800字(8分) + 3个标签(6分) + 1个版本(5分) = 19分
    - Skill B: README 1500字(10分) + 5个标签(10分) + 2个版本(5分) = 25分（满分）
    """
    readme_score = min(len(skill_data.get("readme_content", "")) / 100, 10)
    tag_score = min(len(skill_data.get("tags", [])) * 2, 10)
    version_score = min(len(skill_data.get("versions", [])) * 5, 5)
    return readme_score + tag_score + version_score


# ============================================================
# 3. 创新程度 (20分) - 衡量技术独特性和复杂度
# ============================================================


def calculate_innovation_score(skill_data):
    """
    创新程度 = min(Σ(复杂标签匹配 × 5), 20)

    计算逻辑：
    - 定义复杂技术标签：["Agent开发", "MCP开发", "自动化开发"]
    - 每匹配一个复杂标签得5分
    - 上限20分（即匹配4个即可满分）

    示例：
    - Skill A: 标签["技术开发", "通用开发"] → 0分（无复杂标签）
    - Skill B: 标签["Agent开发", "自动化开发"] → 2 × 5 = 10分
    - Skill C: 标签["Agent开发", "MCP开发", "自动化开发"] → 3 × 5 = 15分
    - Skill D: 标签["Agent开发", "MCP开发", "自动化开发", "智航开发", "飞书开发"] → min(3 × 5, 20) = 15分
      （只有3个复杂标签，其他标签不算）
    """
    complex_tags = ["Agent开发", "MCP开发", "自动化开发"]
    tags = skill_data.get("tags", [])
    innovation_score = sum(5 for tag in tags if any(ct in tag for ct in complex_tags))
    return min(innovation_score, 20)


# ============================================================
# 4. 推广效果 (15分) - 衡量Skill被发现和传播的程度
# ============================================================


def calculate_promotion_score(stats):
    """
    推广效果 = min(搜索曝光 × 0.5 + 收藏数 × 2 + 分享数 × 3, 15)

    计算逻辑：
    - 搜索曝光：每次搜索命中得0.5分
    - 收藏：每次收藏得2分
    - 分享：每次分享得3分
    - 上限15分

    示例：
    - Skill A: 搜索10次 + 收藏2次 + 分享1次 → 10×0.5 + 2×2 + 1×3 = 5+4+3 = 12分
    - Skill B: 搜索20次 + 收藏5次 + 分享2次 → min(20×0.5 + 5×2 + 2×3, 15) = min(10+10+6, 15) = 15分（满分）
    - Skill C: 无推广数据 → 0分
    """
    return min(
        stats.get("search_appearances", 0) * 0.5
        + stats.get("total_favorites", 0) * 2
        + stats.get("total_shares", 0) * 3,
        15,
    )


# ============================================================
# 5. 维护活跃度 (5分) - 衡量Skill的维护更新情况
# ============================================================


def calculate_maintenance_score(skill_data):
    """
    维护活跃度 = max(0, 5 - 更新天数/30)

    计算逻辑：
    - 最近更新天数越短，得分越高
    - 刚更新（0天）：5分（满分）
    - 30天前更新：5 - 30/30 = 4分
    - 150天前更新：5 - 150/30 = 0分
    - 超过150天：0分

    示例：
    - Skill A: 昨天更新 → 5 - 1/30 = 4.97分 ≈ 5分（满分）
    - Skill B: 30天前更新 → 5 - 30/30 = 4分
    - Skill C: 90天前更新 → 5 - 90/30 = 2分
    - Skill D: 180天前更新 → max(0, 5 - 180/30) = 0分
    """
    from datetime import datetime

    update_days = (
        datetime.now()
        - datetime.fromisoformat(
            skill_data.get("updated_at", datetime.now().isoformat())
        )
    ).days
    return max(0, 5 - update_days / 30)


